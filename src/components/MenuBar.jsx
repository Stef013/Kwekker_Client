export default function MenuBar() {
    const classes = useStyles();
    const history = useHistory();
    const [open, setOpen] = React.useState(true);

    const handleDrawerOpen = () => {
        setOpen(true);
    };
    const handleDrawerClose = () => {
        setOpen(false);
    };

    const [darkState, setDarkState] = React.useState(false);
    const palletType = darkState ? "light" : "dark";
    const mainPrimaryColor = darkState ? "#03dac5" : "#03dac5";
    const darkTheme = createMuiTheme({
        palette: {
            type: palletType,
            primary: {
                main: mainPrimaryColor
            }
        }
    });
    const handleThemeChange = () => {
        setDarkState(!darkState);
    };

    const PAGE_TITLES = [
        { page: "/", title: "Home" },
        { page: "/profile", title: "Profile" },
        { page: "/messages", title: "Messages" },
    ]

    useEffect(() => {
        let title = PAGE_TITLES.find(el => el.page === history.location.pathname)?.title
        document.title = title;
        console.log(title);
    }, []);

    return (
        <ThemeProvider theme={darkTheme}>
            <AppBar color="background" position="absolute" className={clsx(classes.appBar, open && classes.appBarShift)}>
                <Toolbar className={classes.toolbar}>
                    <IconButton
                        edge="start"
                        color="inherit"
                        aria-label="open drawer"
                        onClick={handleDrawerOpen}
                        className={clsx(classes.menuButton, open && classes.menuButtonHidden)}
                    >
                        <MenuIcon />
                    </IconButton>
                    <Typography component="h1" variant="h6" color="inherit" noWrap className={classes.title}>
                        {PAGE_TITLES.find(el => el.page === history.location.pathname)?.title}
                    </Typography>
                    <div className={classes.search}>
                        <div className={classes.searchIcon}>
                            <SearchIcon />
                        </div>
                        <InputBase
                            placeholder="Searchâ€¦"
                            classes={{
                                root: classes.inputRoot,
                                input: classes.inputInput,
                            }}
                            inputProps={{ 'aria-label': 'search' }}
                        />
                    </div>
                    <Switch color="primary" checked={darkState} onChange={handleThemeChange} />
                </Toolbar>
            </AppBar>
            <Drawer
                variant="permanent"
                classes={{
                    paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose),
                }}
                open={open}
            >

                <div className={classes.toolbarIcon}>
                    <Icon icon={duckIcon} className={classes.duckIcon} />
                    <Typography variant="h5" style={{ marginRight: 20 }}>Kwekker</Typography>
                    <IconButton onClick={handleDrawerClose}>
                        <ChevronLeftIcon />
                    </IconButton>
                </div>
                <Divider />
                <MainListItems />
                <Divider />
                <SecondaryListItems />
            </Drawer>
        </ThemeProvider>
    )
}